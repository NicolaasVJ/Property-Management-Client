    public void populatetblClient()
    {
        Client client= new Client();
        ArrayList<Client> clientList=client.clientList();
        
        String[] colName={"ID","First Name","Last Name","Phone","Email","Address"};//col
        Object[][] rows=new Object[clientList.size()][6];//row and whats in row
        
        for(int x=0;x<clientList.size();x++)
        {
            rows[x][0]=clientList.get(x).getId();
            rows[x][1]=clientList.get(x).getFName();
            rows[x][2]=clientList.get(x).getSurname();
            rows[x][3]=clientList.get(x).getCell();
            rows[x][4]=clientList.get(x).getEmail();
            rows[x][5]=clientList.get(x).getAddress();
            
        }
        
        DefaultTableModel model=new DefaultTableModel(rows,colName);
        tblClient.setModel(model);
        tblClient.setRowHeight((35));
        tblClient.getColumnModel().getColumn(0).setPreferredWidth(30);
        tblClient.getColumnModel().getColumn(5).setPreferredWidth(130);  
    }
