    private void btnAddClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddClientActionPerformed
        //add client
        String fname=txtFName.getText();
        String lname=txtLName.getText();
        String phone=txtPhone.getText();
        String email=txtEmail.getText();
        String address=txtAddress.getText();

        Client client=new Client();

        //check if empty

        if(fname.trim().equals("") || lname.trim().equals("") || phone.trim().equals("") || email.trim().equals("") || address.trim().equals(""))
        {
            JOptionPane.showMessageDialog(null, "One or more Client details are empty","Add Client details",2);
        }
        else
        {
            if(client.addClient(new Client(0,fname,lname,phone,email,address)))
            {
                JOptionPane.showMessageDialog(null, "New Client has successfully been added","Add New Client",1);
                clearFields();
                 populatetblClient();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Client has not been added","Add New Client",2);
                
                
            }
        }

    }//GEN-LAST:event_btnAddClientActionPerformed

    private void btnEditClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditClientActionPerformed

        String fname=txtFName.getText();
        String lname=txtLName.getText();
        String phone=txtPhone.getText();
        String email=txtEmail.getText();
        String address=txtAddress.getText();

        Client client=new Client();

        try
        {
            int clientId=Integer.valueOf(txtId.getText());

            if(fname.trim().equals("") || lname.trim().equals("") || phone.trim().equals("") || email.trim().equals("") || address.trim().equals(""))
            {
                JOptionPane.showMessageDialog(null, "1 or more Client details empty","Edit Client details",2);
            }
            else
            {
                if(client.editClient(new Client(clientId,fname,lname,phone,email,address)))
                {
                    JOptionPane.showMessageDialog(null, "New Client has successfully been edited","EditClient",1);
                    populatetblClient();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Client not been edited","Edit Client",2);
                }
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex.getMessage()+ "Select ID", "ID is empty",0);
        }

    }//GEN-LAST:event_btnEditClientActionPerformed

    private void btnRemoveClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveClientActionPerformed

        try
        {
            int clientId=Integer.valueOf(txtId.getText());

            Client client=new Client();

            if(txtId.getText().trim().equals(""))
            {
                JOptionPane.showMessageDialog(null, "Client ID empty","Add ID",2);
            }
            else
            {if(client.removeClient(clientId))
                {
                JOptionPane.showMessageDialog(null, "Client ID deleted","Removed ID",2);
                clearFields();
                 populatetblClient();
                }
            else{
                JOptionPane.showMessageDialog(null, "Deletion failed","Removed ID",2);
            }
            }

        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex.getMessage()+ "Select ID", "ID is empty",0);
        }

    }//GEN-LAST:event_btnRemoveClientActionPerformed

